// 2. Localized Text Fetcher
export function t(key) {
    const lang = localStorage.getItem('suuqlink_lang') || 'en';
    return DICTIONARY[lang][key] || key;
}

// 3. Current Language Getter/Setter
export function getLanguage() {
    return localStorage.getItem('suuqlink_lang') || 'en';
}

export function setLanguage(lang) {
    if (lang === 'en' || lang === 'so') {
        localStorage.setItem('suuqlink_lang', lang);
        window.dispatchEvent(new Event('languagechange'));
    }
}

// 4. Currency and Date Formatters
export function formatCurrency(amount) {
    return new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: 'USD'
    }).format(amount);
}

export function formatDate(dateString) {
    if (!dateString) return '';
    const d = new Date(dateString);
    return d.toLocaleDateString('en-US', {
        month: 'short',
        day: 'numeric',
        year: 'numeric'
    });
}

// 5. Beautiful Toast Notification Alerts Injection Engine
export function showToast(message, type = 'success') {
    // Check if alert container exists, otherwise create it
    let container = document.getElementById('suuqlink-toast-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'suuqlink-toast-container';
        container.className = 'alert-container';
        document.body.appendChild(container);
    }

    // Create Toast element
    const toast = document.createElement('div');
    toast.className = `alert-toast ${type}`;
    
    let icon = 'check_circle';
    if (type === 'error') icon = 'error';
    if (type === 'warning') icon = 'warning';

    toast.innerHTML = `
        <span class="material-icons-round" style="color: inherit;">${icon}</span>
        <div style="flex: 1;">
            <p class="text-sm font-semibold" style="margin: 0;">${message}</p>
        </div>
    `;

    container.appendChild(toast);

    // Auto-remove after 4 seconds
    setTimeout(() => {
        toast.style.animation = 'slideIn 0.3s ease reverse forwards';
        setTimeout(() => {
            toast.remove();
        }, 300);
    }, 4000);
}

// 6. Dynamic Text Translation Resolver for Bilingual Fields
export function getLocalizedText(obj, fieldBase) {
    const lang = getLanguage();
    if (!obj) return '';
    return obj[`${fieldBase}_${lang}`] || obj[`${fieldBase}_en`] || obj[fieldBase] || '';
}
